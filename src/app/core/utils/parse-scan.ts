export function groupTextBlocksByPosition(textBlocks: any[], n = 20): any[] {
    const groups: any[] = [];
    textBlocks.forEach((block, ix) => {
        if (ix === 0) {
            return;
        }
        const boundingPoly = block.boundingPoly.vertices;
        const y = boundingPoly[0].y;
        let group = groups.find((g) => Math.abs(g.y - y) < n);
        if (!group) {
            group = { y, blocks: [] };
            groups.push(group);
        }
        group.blocks.push(block);
    });
    for (const g of groups) {
        g.blocks.sort((a: any, b: any) => a.boundingPoly.vertices[0].x - b.boundingPoly.vertices[0].x);
    }
    return groups;
}
